    <li>
        Menyalahgunakan wewenang;
    </li>
    <li>
        Menjadi perantara untuk mendapatkan keuntungan pribadi dan / atau orang lain dengan menggunakan kewenangan orang
        lain;
    </li>
    <li>
        Tanpa izin pemerintah menjadi pegawai atau bekerja untuk negara lain dan / atau lembaga organisasi

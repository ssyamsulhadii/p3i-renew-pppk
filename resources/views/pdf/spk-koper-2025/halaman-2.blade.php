    </li>
    <li>
        Bekerja pada perusahaan asing, konsultan asing, atau lembaga swadaya masyarakat asing;
    </li>
    <li>
        Memiliki, menjual, membeli, menggadaikan, menyewakan atau meminjamkan barang-barang baik bergerak atau tidak
        bergerak, dokumen atau surat berharga milik negara secara tidak sah;
    </li>
    <li>
        Melakukan kegiatan bersama dengan atasan, teman sejawat, bawahan atau orang lain di dalam maupun diluar
        lingkungan kerjanya dengan tujuan untuk keuntungan pribadi, golongan atau pihak lain yang secara langsung atau
        tidak langsung merugikan negara;
    </li>
    <li>
        Memberikan atau menyanggupi akan memberi sesuatu kepada siapapun baik secara langsung atau tidak langsung dan
        dengan dalih apapun untuk diangkat dalam jabatan;
    </li>
    <li>
        Menerima hadiah atau suatu pemberian apa saja dari siapapun juga yang berhubungan dengan jabatan dan / atau
        pekerjaannya;
    </li>
    <li>
        Bertindak sewenang-wenang terhadap bawahannya;
    </li>
    <li>
        Melakukan suatu tindakan atau tidak melakukan suatu tindakan yang dapat menghalangi atau mempersulit salah satu
        pihak yang dilayani sehingga mengakibatkan kerugian bagi yang dilayani;
    </li>
    <li>
        Menghalangi berjalannya tugas kedinasan;
    </li>
    <li>
        Memberikan dukungan kepada Calon Presiden / Wakil Presiden, Dewan Perwakilan Rakyat, Dewan Perwakilan Daerah,
        atau Dewan Perwakilan Rakyat Daerah dengan cara :
    </li>
    <br> <br> <br>
</x-pdf.ul>
